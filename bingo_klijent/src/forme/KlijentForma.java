    public void podesi_tabelu(int namestu, int nije) {
        potezi.add(new Potez(null,brpok, namestu, nije, String.join("-", new String[]{jLabel1.getText(),jLabel2.getText(),jLabel3.getText(),jLabel4.getText()})));
         m.getPotezi().clear();
        for(int i=0;i<potezi.size()-1;i++)
        {
            m.getPotezi().add(potezi.get(i));
        }
        m.fireTableDataChanged();
     }
