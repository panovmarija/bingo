    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (t1.isRunning() || t2.isRunning() || t3.isRunning() || t4.isRunning()) {
            JOptionPane.showMessageDialog(this, "prvo odaberi brojeve");
            return;
        }
        brpok++;
        int[] pog = new int[]{Integer.valueOf(jLabel1.getText()), Integer.valueOf(jLabel2.getText()), Integer.valueOf(jLabel3.getText()), Integer.valueOf(jLabel4.getText())};
        komunikacija.Komunikacija.getInstance().posaljiZahtev(new KlijentZahtev(pog, Operacije.POGADJA));
        ServerOdg so=komunikacija.Komunikacija.getInstance().procitajOdgovor();
        int []stat=(int[]) so.getOdg();
        int namestu=stat[0];
        int nije=stat[1];
        jLabel_namestu.setText(namestu+"");
        jLabel_nije.setText(nije+"");
//        PODESAVNJE TABELE
        podesi_tabelu(namestu,nije);
//        
        if(namestu==4)
        {
            JOptionPane.showMessageDialog(this, "pobeda");
            jButton1.setEnabled(false);
            Igra i=new Igra(-1, new Date(), null, "pobedio", brpok);
            komunikacija.Komunikacija.getInstance().posaljiZahtev(new KlijentZahtev(new WrapIgraPotezi(i, potezi), Operacije.GOTOVA_IGRA));
            return ;
        }
        if(brpok==6){
            JOptionPane.showMessageDialog(this, "soriska izgubio si");
            jButton1.setEnabled(false);
            Igra i=new Igra(-1, new Date(), null, "izgubio", brpok);
            komunikacija.Komunikacija.getInstance().posaljiZahtev(new KlijentZahtev(new WrapIgraPotezi(i, potezi), Operacije.GOTOVA_IGRA));
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
